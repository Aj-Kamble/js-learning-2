console.log("<========Swap Two values Using Third variables=========>");

var sweety = "Sweety";
var cutie = "Cutie";

console.log("Before Swapping Value===>", "Sweety:"+sweety, "Cutie:"+cutie);
var temp;
temp=sweety;
sweety=cutie;
cutie=temp;
console.log("After Swapping Value====>", "Sweety:"+sweety, "Cutie:"+cutie);

